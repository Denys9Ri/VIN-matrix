_HAS_RUN = False


def _run(cursor, sql):
    try:
        cursor.execute(sql)
    except Exception as exc:
        print(f"CRM legacy schema repair skipped SQL: {exc}")


def repair_crm_legacy_schema(verbose=False):
    global _HAS_RUN
    if _HAS_RUN:
        return
    _HAS_RUN = True

    with connection.cursor() as cursor:
        for label, sql in _CREATE_TABLES:
            _run(cursor, sql)
            if verbose:
                print(f"CRM legacy repair table checked: {label}")

        for sql in _ALTER_TABLES:
            _run(cursor, sql)

        for sql in _CLEANUP:
            _run(cursor, sql)

    if verbose:
        print("CRM legacy schema repair finished")
