from django.db import connection

_HAS_RUN = False


def _run(cursor, sql):
    try:
        cursor.execute(sql)
    except Exception as exc:
        print(f"Payment schema check skipped SQL: {exc}")


def repair_payment_schema(verbose=False):
    global _HAS_RUN
    if _HAS_RUN:
        return
    _HAS_RUN = True
    with connection.cursor() as cursor:
        _run(cursor, """
            CREATE TABLE IF NOT EXISTS core_visitpayment (
                id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                amount numeric(10, 2) NOT NULL DEFAULT 0,
                payment_type varchar(30) NOT NULL DEFAULT 'cash',
                payment_purpose varchar(30) NOT NULL DEFAULT 'partial',
                comment text NULL,
                created_at timestamp with time zone NOT NULL DEFAULT NOW(),
                company_id bigint NOT NULL,
                visit_id bigint NOT NULL,
                created_by_id integer NULL
            );
        """)
        _run(cursor, "ALTER TABLE core_visitpayment ADD COLUMN IF NOT EXISTS amount numeric(10, 2) NOT NULL DEFAULT 0;")
        _run(cursor, "ALTER TABLE core_visitpayment ADD COLUMN IF NOT EXISTS payment_type varchar(30) NOT NULL DEFAULT 'cash';")
        _run(cursor, "ALTER TABLE core_visitpayment ADD COLUMN IF NOT EXISTS payment_purpose varchar(30) NOT NULL DEFAULT 'partial';")
        _run(cursor, "ALTER TABLE core_visitpayment ADD COLUMN IF NOT EXISTS comment text NULL;")
        _run(cursor, "ALTER TABLE core_visitpayment ADD COLUMN IF NOT EXISTS created_at timestamp with time zone NOT NULL DEFAULT NOW();")
        _run(cursor, "ALTER TABLE core_visitpayment ADD COLUMN IF NOT EXISTS company_id bigint;")
        _run(cursor, "ALTER TABLE core_visitpayment ADD COLUMN IF NOT EXISTS visit_id bigint;")
        _run(cursor, "ALTER TABLE core_visitpayment ADD COLUMN IF NOT EXISTS created_by_id integer NULL;")
        _run(cursor, "CREATE INDEX IF NOT EXISTS core_visitpayment_visit_idx ON core_visitpayment (visit_id, created_at);")
        _run(cursor, "CREATE INDEX IF NOT EXISTS core_visitpayment_company_idx ON core_visitpayment (company_id, created_at);")
        _run(cursor, """
            INSERT INTO core_visitpayment (company_id, visit_id, amount, payment_type, payment_purpose, comment, created_at)
            SELECT v.company_id, v.id, v.prepayment_amount, 'cash', 'legacy', 'Перенесено зі старої передплати', COALESCE(v.created_at, NOW())
            FROM core_visit v
            WHERE COALESCE(v.prepayment_amount, 0) > 0
              AND NOT EXISTS (SELECT 1 FROM core_visitpayment p WHERE p.visit_id = v.id)
        """)
    if verbose:
        print("Payment schema check finished")
