import os
import django

os.environ.setdefault("DJANGO_SETTINGS_MODULE", "vin_matrix.settings")
django.setup()

from django.db import connection


def run_sql(cursor, sql, label=None):
    try:
        cursor.execute(sql)
        if label:
            print(f"✅ {label}")
    except Exception as e:
        print(f"⚠️ {label or sql}: {e}")


print("Починаємо ремонт бази даних PostgreSQL...")

# Таблиці, які мають існувати
queries_create = [
    (
        "core_employee",
        """
        CREATE TABLE IF NOT EXISTS core_employee (
            id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            role varchar(20) NOT NULL DEFAULT 'mechanic',
            can_create_visits boolean NOT NULL DEFAULT false,
            can_view_finances boolean NOT NULL DEFAULT false,
            partner_code varchar(20) NULL,
            company_id bigint NOT NULL REFERENCES core_company(id) DEFERRABLE INITIALLY DEFERRED,
            user_id integer NOT NULL UNIQUE REFERENCES auth_user(id) DEFERRABLE INITIALLY DEFERRED
        );
        """,
    ),
    (
        "core_category",
        """
        CREATE TABLE IF NOT EXISTS core_category (
            id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            name varchar(255) NOT NULL,
            company_id bigint NOT NULL REFERENCES core_company(id) DEFERRABLE INITIALLY DEFERRED
        );
        """,
    ),
    (
        "core_inventoryitem",
        """
        CREATE TABLE IF NOT EXISTS core_inventoryitem (
            id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            brand varchar(100) NOT NULL,
            article varchar(100) NOT NULL,
            name text NOT NULL,
            quantity integer NOT NULL DEFAULT 0,
            buy_price numeric(10, 2) NOT NULL,
            category_id bigint REFERENCES core_category(id) ON DELETE SET NULL DEFERRABLE INITIALLY DEFERRED,
            company_id bigint NOT NULL REFERENCES core_company(id) DEFERRABLE INITIALLY DEFERRED
        );
        """,
    ),
    (
        "core_supplier",
        """
        CREATE TABLE IF NOT EXISTS core_supplier (
            id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            name varchar(255) NOT NULL,
            api_key varchar(255),
            price_file varchar(100),
            warehouse_prefs jsonb NOT NULL DEFAULT '[]'::jsonb,
            company_id bigint NOT NULL REFERENCES core_company(id) DEFERRABLE INITIALLY DEFERRED
        );
        """,
    ),
    (
        "core_servicecomplex",
        """
        CREATE TABLE IF NOT EXISTS core_servicecomplex (
            id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            name varchar(255) NOT NULL,
            description text NULL,
            is_active boolean NOT NULL DEFAULT true,
            created_at timestamp with time zone NOT NULL DEFAULT NOW(),
            updated_at timestamp with time zone NOT NULL DEFAULT NOW(),
            company_id bigint NOT NULL REFERENCES core_company(id) ON DELETE CASCADE DEFERRABLE INITIALLY DEFERRED
        );
        """,
    ),
    (
        "core_complexserviceitem",
        """
        CREATE TABLE IF NOT EXISTS core_complexserviceitem (
            id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            name varchar(255) NOT NULL,
            price numeric(10, 2) NOT NULL DEFAULT 0,
            quantity numeric(8, 2) NOT NULL DEFAULT 1,
            complex_id bigint NOT NULL REFERENCES core_servicecomplex(id) ON DELETE CASCADE DEFERRABLE INITIALLY DEFERRED
        );
        """,
    ),
    (
        "core_complexpartitem",
        """
        CREATE TABLE IF NOT EXISTS core_complexpartitem (
            id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            name varchar(255) NOT NULL,
            brand varchar(100) NULL,
            article varchar(100) NULL,
            buy_price numeric(10, 2) NOT NULL DEFAULT 0,
            sell_price numeric(10, 2) NOT NULL DEFAULT 0,
            quantity numeric(8, 2) NOT NULL DEFAULT 1,
            supplier varchar(100) NULL,
            complex_id bigint NOT NULL REFERENCES core_servicecomplex(id) ON DELETE CASCADE DEFERRABLE INITIALLY DEFERRED
        );
        """,
    ),
    (
        "crm_client",
        """
        CREATE TABLE IF NOT EXISTS crm_client (
            id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            full_name varchar(255) NOT NULL,
            phone_number varchar(20),
            company_id bigint NOT NULL REFERENCES core_company(id) DEFERRABLE INITIALLY DEFERRED
        );
        """,
    ),
    (
        "crm_servicecatalog",
        """
        CREATE TABLE IF NOT EXISTS crm_servicecatalog (
            id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            name varchar(255) NOT NULL,
            default_price numeric(10, 2) NOT NULL DEFAULT 0,
            is_active boolean NOT NULL DEFAULT true,
            company_id bigint NOT NULL REFERENCES core_company(id) DEFERRABLE INITIALLY DEFERRED
        );
        """,
    ),
    (
        "crm_visitservice",
        """
        CREATE TABLE IF NOT EXISTS crm_visitservice (
            id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            custom_name varchar(255) NOT NULL,
            price numeric(10, 2) NOT NULL DEFAULT 0,
            quantity numeric(5, 1) NOT NULL DEFAULT 1,
            service_catalog_id bigint REFERENCES crm_servicecatalog(id) ON DELETE SET NULL DEFERRABLE INITIALLY DEFERRED,
            visit_id bigint REFERENCES crm_visit(id) ON DELETE CASCADE DEFERRABLE INITIALLY DEFERRED
        );
        """,
    ),
]

# Колонки, які мають існувати. IF NOT EXISTS важливо, щоб скрипт можна було запускати багато разів.
queries_alter = [
    # core_employee
    "ALTER TABLE core_employee ADD COLUMN IF NOT EXISTS can_create_visits boolean NOT NULL DEFAULT false;",
    "ALTER TABLE core_employee ADD COLUMN IF NOT EXISTS can_view_finances boolean NOT NULL DEFAULT false;",
    "ALTER TABLE core_employee ADD COLUMN IF NOT EXISTS partner_code varchar(20) NULL;",
    "CREATE UNIQUE INDEX IF NOT EXISTS core_employee_partner_code_unique ON core_employee (partner_code) WHERE partner_code IS NOT NULL;",

    # core_supplier
    "ALTER TABLE core_supplier ADD COLUMN IF NOT EXISTS warehouse_prefs jsonb NOT NULL DEFAULT '[]'::jsonb;",
    "ALTER TABLE core_supplier ADD COLUMN IF NOT EXISTS api_key varchar(255) NULL;",
    "ALTER TABLE core_supplier ADD COLUMN IF NOT EXISTS price_file varchar(100) NULL;",

    # core_company
    "ALTER TABLE core_company ADD COLUMN IF NOT EXISTS euro_rate numeric(6, 2) NOT NULL DEFAULT 42.00;",
    "ALTER TABLE core_company ADD COLUMN IF NOT EXISTS business_type varchar(20) NOT NULL DEFAULT 'sto';",
    "ALTER TABLE core_company ADD COLUMN IF NOT EXISTS phone varchar(50) NULL;",
    "ALTER TABLE core_company ADD COLUMN IF NOT EXISTS address varchar(255) NULL;",
    "ALTER TABLE core_company ADD COLUMN IF NOT EXISTS document_footer text NULL;",
    "ALTER TABLE core_company ADD COLUMN IF NOT EXISTS global_margin_percent numeric(5, 2) NOT NULL DEFAULT 20.00;",
    "ALTER TABLE core_company ADD COLUMN IF NOT EXISTS logo varchar(100) NULL;",

    # core_visit
    "ALTER TABLE core_visit ADD COLUMN IF NOT EXISTS updated_at timestamp with time zone;",
    "ALTER TABLE core_visit ADD COLUMN IF NOT EXISTS scheduled_datetime timestamp with time zone;",
    "ALTER TABLE core_visit ADD COLUMN IF NOT EXISTS comment text;",
    "ALTER TABLE core_visit ADD COLUMN IF NOT EXISTS vin_code varchar(17);",
    "ALTER TABLE core_visit ADD COLUMN IF NOT EXISTS mileage integer;",
    "ALTER TABLE core_visit ADD COLUMN IF NOT EXISTS delivery_type varchar(50) DEFAULT 'pickup';",
    "ALTER TABLE core_visit ADD COLUMN IF NOT EXISTS delivery_data text;",
    "ALTER TABLE core_visit ADD COLUMN IF NOT EXISTS payment_status varchar(50) DEFAULT 'unpaid';",
    "ALTER TABLE core_visit ADD COLUMN IF NOT EXISTS prepayment_amount numeric(10, 2) DEFAULT 0;",

    # complex tables
    "ALTER TABLE core_servicecomplex ADD COLUMN IF NOT EXISTS description text NULL;",
    "ALTER TABLE core_servicecomplex ADD COLUMN IF NOT EXISTS is_active boolean NOT NULL DEFAULT true;",
    "ALTER TABLE core_servicecomplex ADD COLUMN IF NOT EXISTS created_at timestamp with time zone NOT NULL DEFAULT NOW();",
    "ALTER TABLE core_servicecomplex ADD COLUMN IF NOT EXISTS updated_at timestamp with time zone NOT NULL DEFAULT NOW();",
    "ALTER TABLE core_complexserviceitem ADD COLUMN IF NOT EXISTS quantity numeric(8, 2) NOT NULL DEFAULT 1;",
    "ALTER TABLE core_complexpartitem ADD COLUMN IF NOT EXISTS buy_price numeric(10, 2) NOT NULL DEFAULT 0;",
    "ALTER TABLE core_complexpartitem ADD COLUMN IF NOT EXISTS sell_price numeric(10, 2) NOT NULL DEFAULT 0;",
    "ALTER TABLE core_complexpartitem ADD COLUMN IF NOT EXISTS quantity numeric(8, 2) NOT NULL DEFAULT 1;",
    "ALTER TABLE core_complexpartitem ADD COLUMN IF NOT EXISTS supplier varchar(100) NULL;",

    # order tables
    "ALTER TABLE core_orderpart ADD COLUMN IF NOT EXISTS status varchar(20) DEFAULT 'WAITING';",
    "ALTER TABLE core_orderservice ADD COLUMN IF NOT EXISTS status varchar(20) DEFAULT 'PENDING';",

    # platform clients
    "ALTER TABLE core_platformclient ADD COLUMN IF NOT EXISTS referred_by_id integer NULL REFERENCES auth_user(id) DEFERRABLE INITIALLY DEFERRED;",
    "ALTER TABLE core_platformclient ADD COLUMN IF NOT EXISTS payment_status varchar(20) NOT NULL DEFAULT 'pending';",
    "ALTER TABLE core_platformclient ADD COLUMN IF NOT EXISTS is_access_enabled boolean NOT NULL DEFAULT false;",
    "ALTER TABLE core_platformclient ADD COLUMN IF NOT EXISTS created_at timestamp with time zone DEFAULT NOW();",

    # crm tables, якщо вони є
    "ALTER TABLE crm_visit ADD COLUMN IF NOT EXISTS mileage integer;",
]

with connection.cursor() as cursor:
    for label, sql in queries_create:
        run_sql(cursor, sql, f"Таблиця {label} ОК")

    for sql in queries_alter:
        run_sql(cursor, sql)

    # Заповнюємо дефолтні значення там, де старі записи могли мати NULL
    cleanup_queries = [
        "UPDATE core_employee SET role = 'mechanic' WHERE role IS NULL;",
        "UPDATE core_employee SET can_create_visits = false WHERE can_create_visits IS NULL;",
        "UPDATE core_employee SET can_view_finances = false WHERE can_view_finances IS NULL;",
        "UPDATE core_supplier SET warehouse_prefs = '[]'::jsonb WHERE warehouse_prefs IS NULL;",
        "UPDATE core_company SET euro_rate = 42.00 WHERE euro_rate IS NULL;",
        "UPDATE core_company SET business_type = 'sto' WHERE business_type IS NULL;",
        "UPDATE core_company SET global_margin_percent = 20.00 WHERE global_margin_percent IS NULL;",
        "UPDATE core_servicecomplex SET is_active = true WHERE is_active IS NULL;",
        "UPDATE core_servicecomplex SET created_at = NOW() WHERE created_at IS NULL;",
        "UPDATE core_servicecomplex SET updated_at = NOW() WHERE updated_at IS NULL;",
        "UPDATE core_platformclient SET payment_status = 'pending' WHERE payment_status IS NULL;",
        "UPDATE core_platformclient SET is_access_enabled = false WHERE is_access_enabled IS NULL;",
        "UPDATE core_platformclient SET created_at = NOW() WHERE created_at IS NULL;",
    ]
    for sql in cleanup_queries:
        run_sql(cursor, sql)

print("✅ Ремонт бази завершено. Можна перезапускати backend.")
