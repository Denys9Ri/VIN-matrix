const escapeHtml = (value) => String(value).replace(/[&<>"']/g, (character) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[character]));
const canonical = (path) => `${origin}${path === '/' ? '/' : path}`;

function relatedLinks(currentPath) {
  return allSolutions
    .filter(([path]) => path !== currentPath)
    .slice(0, 3)
    .map(([path, label]) => `<li><a href="${path}">${escapeHtml(label)}</a></li>`)
    .join('');
}

function enrichment(page) {
  const workflow = page.workflow.map((item, index) => `<li><strong>${index + 1}.</strong> ${escapeHtml(item)}</li>`).join('');
  const faq = page.faq.map(([question, answer]) => `<details><summary>${escapeHtml(question)}</summary><p>${escapeHtml(answer)}</p></details>`).join('');

  return `<section class="vin-static-section vin-static-enrichment" aria-labelledby="vin-problem-${page.path === '/' ? 'home' : page.path.slice(1)}">
    <h2 id="vin-problem-${page.path === '/' ? 'home' : page.path.slice(1)}">${escapeHtml(page.problemTitle)}</h2>
    <p>${escapeHtml(page.problem)}</p>
    <h2>${escapeHtml(page.workflowTitle)}</h2>
    <ol class="vin-static-workflow">${workflow}</ol>
    <h2>Поширені питання</h2>
    <div class="vin-static-faq">${faq}</div>
    <h2>Пов’язані рішення</h2>
    <ul class="vin-static-links">${relatedLinks(page.path)}</ul>
  </section>
  <style>
    .vin-static-enrichment h2{margin:28px 0 12px}.vin-static-workflow{padding-left:24px}.vin-static-workflow li{margin:12px 0}.vin-static-workflow strong{color:#2e66ed}.vin-static-faq details{padding:14px 0;border-top:1px solid #d9e5f4}.vin-static-faq summary{cursor:pointer;font-weight:800}.vin-static-faq p{margin:10px 0 0}
  </style>`;
}

function breadcrumbSchema(page) {
  if (page.path === '/') return '';
  const url = canonical(page.path);
  const data = {
    '@context': 'https://schema.org',
    '@type': 'BreadcrumbList',
    '@id': `${url}#breadcrumb`,
    itemListElement: [
      { '@type': 'ListItem', position: 1, name: 'VIN-matrix', item: `${origin}/` },
      { '@type': 'ListItem', position: 2, name: page.label, item: url },
    ],
  };
  return `<script id="vin-matrix-breadcrumb-jsonld" type="application/ld+json">${JSON.stringify(data).replace(/</g, '\\u003c')}</script>`;
}

for (const page of pages) {
  const file = page.path === '/' ? join(distDir, 'index.html') : join(distDir, page.path.slice(1), 'index.html');
  if (!existsSync(file)) throw new Error(`Static page is missing: ${page.path}`);

  let html = readFileSync(file, 'utf8');
  if (!html.includes('vin-static-snapshot')) throw new Error(`Static snapshot is missing: ${page.path}`);
  if (html.includes('vin-static-enrichment')) throw new Error(`Static page was enriched twice: ${page.path}`);

  html = html.replace('</main>', `${enrichment(page)}</main>`);
  const breadcrumb = breadcrumbSchema(page);
  if (breadcrumb) html = html.replace('</head>', `${breadcrumb}</head>`);
  writeFileSync(file, html, 'utf8');
}

console.log(`Enriched ${pages.length} public static pages with useful content and breadcrumbs.`);
